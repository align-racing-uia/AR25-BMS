void UpdateSimulation(Simulation_HandleTypeDef *simulation, Simulation_RX_TypeDef *rx_data)
{
    simulation->cell_voltages[0] = ((float) rx_data->cell_voltages[0]) / 10000.0f;
    simulation->cell_voltages[1] = ((float) rx_data->cell_voltages[1]) / 10000.0f;
    simulation->cell_voltages[2] = ((float) rx_data->cell_voltages[2]) / 10000.0f;

    simulation->cell_temperatures[0] = ((float) rx_data->cell_temperatures[0]) / 10.0f;
    simulation->cell_temperatures[1] = ((float) rx_data->cell_temperatures[1]) / 10.0f;
    simulation->cell_temperatures[2] = ((float) rx_data->cell_temperatures[2]) / 10.0f;

    simulation->current = ((float) rx_data->current) / 100.0f; // Convert to A
    simulation->last_timestamp = rx_data->timestamp; // Update the last timestamp
    return;
}

